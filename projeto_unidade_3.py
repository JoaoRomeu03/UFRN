import numpy as np
import matplotlib.pyplot as plt

a = 1
b = 0.25
c = 0.01
d = 0.02
e = 0.02
h = 0
x0 = 50
y0 = 10
t0 = 0
tf = 200
dt = 0.001
num_steps = int((tf - t0) / dt)

x = np.zeros(num_steps)
y = np.zeros(num_steps)
t = np.linspace(t0, tf, num_steps)

x[0] = x0
y[0] = y0

for n in range(num_steps - 1):
    dx = x[n] * (a - c * x[n] - d * y[n]) * dt
    dy = (-y[n] * (b - e * x[n]) - h) * dt

    x[n + 1] = x[n] + dx
    y[n + 1] = y[n] + dy

plt.figure(figsize=(8, 4))
plt.plot(t, x, label='Presas (x)')
plt.plot(t, y, label='Predadores (y)')
plt.title('Evolução das Populações')
plt.xlabel('Tempo')
plt.ylabel('População')
plt.legend()
plt.grid()

x_values = np.linspace(0, max(x) * 1.1, 20)
y_values = np.linspace(0, max(y) * 1.1, 20)
X, Y = np.meshgrid(x_values, y_values)

U = X * (a - c * X - d * Y)
V = (-Y * (b - e * X) - h)

plt.figure(figsize=(8,4))
plt.quiver(X, Y, U, V, color='blue')
plt.plot(x, y, color='red', label='Trajetória (x/y)')
plt.title('Diagrama de Fase do Sistema Presa-Predador')
plt.xlabel('Presas (x)')
