#Questão 1
import numpy as np
import matplotlib.pyplot as plt

m1 = 20
m2 = 20
k1 = 1000
k2 = 2000
b1 = 1
b2 = 5

t0 = 0
tf = 2000
dt = 0.001

t = np.arange(t0, tf, dt)
x1 = np.zeros(len(t))
x2 = np.zeros(len(t))
v1 = np.zeros(len(t))
v2 = np.zeros(len(t))

def F(n):
  if n == 0:
    return 100
  else:
    return 0

def dv1(n):
  return ((-k1-k2) * x1[n] + (-b1-b2) * v1[n] + k2 * x2[n] + b2 * v2[n])/m1

def dv2(n):
  return  (k2*x1[n] + b2*v1[n] - k2*x2[n] - b2*v2[n] + F(n))/m2

x1[0] = 0
x2[0] = 0
v1[0] = 0
v2[0] = 0

for n in range(len(t)-1):
    v1[n + 1] = v1[n] + dv1(n) * dt
    v2[n + 1] = v2[n] + dv2(n) * dt
    x1[n + 1] = x1[n] + v1[n] * dt
    x2[n + 1] = x2[n] + v2[n] * dt

plt.figure(figsize=(6, 4))
plt.plot(t, x1, label='Posição x1')
plt.plot(t, x2, label='Posição x2')
plt.xlabel('Tempo (s)')
plt.ylabel('Posição (m)')
plt.legend()
plt.title('Gráfico de Posições x1 e x2 em função do tempo')
plt.grid(True)
plt.show()

x1_values = np.linspace(-0.5, 0.5, 20)
x2_values = np.linspace(-0.5, 0.5, 20)
X1, X2 = np.meshgrid(x1_values, x2_values)

U = X2
V = (-k1 * X1 - b1 * X2 + k2 * (X2 - X1)) / m1

plt.figure(figsize=(6, 4))
plt.quiver(X1, X2, U, V, color='blue')
plt.plot(x1, v1, color='red', label='Trajetória x1/v1')
plt.plot(x2, v2, color='green', label='Trajetória x2/v2')
plt.xlabel('Posição (x)')
plt.ylabel('Velocidade (v)')
plt.title('Diagrama de Fase do sistema massa-mola-amortecedor')
plt.legend()
plt.grid(True)
plt.show()

