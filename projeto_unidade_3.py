import numpy as np
import matplotlib.pyplot as plt

L = 2
L0 = 1
k = 40
m = 1
g = 9.81
theta = 3 * np.pi / 4

t0 = 0
tf = 20
dt = 0.001
num_steps = int((tf - t0) / dt)

x1 = np.zeros(num_steps)
x2 = np.zeros(num_steps)
t = np.linspace(t0, tf, num_steps)

x1[0] = 0
x2[0] = 0

for n in range(num_steps - 1):
    dx1 = -(1/L) * (g * np.sin(theta) + 2 * x1[n] * x2[n]) * dt
    dx2 = (1/m) * (m * L * x1[n]**2 - k * (L - L0) + m * g * np.cos(theta)) * dt

    x1[n + 1] = x1[n] + dx1
    x2[n + 1] = x2[n] + dx2

plt.figure(figsize=(8, 4))
plt.plot(t, x1, label='Posição (x1)')
plt.plot(t, x2, label='Velocidade (x2)')
plt.xlabel('Tempo (s)')
plt.ylabel('Magnitude')
plt.legend()
plt.title('Gráfico de Posição e Velocidade')
plt.tight_layout()
plt.show()

